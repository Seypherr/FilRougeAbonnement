export function normalizeLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language) ? language : DEFAULT_LANGUAGE;
}

export function getDictionary(language) {
  return dictionaries[normalizeLanguage(language)] ?? dictionaries[DEFAULT_LANGUAGE] ?? dictionaries.en;
}

export function getLanguageLocale(language) {
  const locales = {
    en: "en-US",
    es: "es-ES",
    fr: "fr-FR"
  };

  return locales[normalizeLanguage(language)] ?? locales[DEFAULT_LANGUAGE];
}

export function translateCategoryName(categoryName, t) {
  if (!categoryName) return t.categoryPlaceholder ?? t.other;

  const key = String(categoryName).trim().toLowerCase();
  return t.categoryNames?.[key] ?? categoryName;
}

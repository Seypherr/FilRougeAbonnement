export function normalizeServiceName(value = "") {
  return value
    .toString()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^\w+\s-]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export const suggestedServices = serviceLogoMappings
  .filter((service) => service.domain)
  .map(({ brand, domain, keywords, category, priceHints }) => ({
    name: brand,
    domain,
    category,
    priceHints,
    aliases: keywords.filter((keyword) => normalizeServiceName(keyword) !== normalizeServiceName(brand))
  }));

export const suggestedPaymentMethods = paymentMethodMappings.map(({ brand, domain, keywords }) => ({
  name: brand,
  domain,
  aliases: keywords.filter((keyword) => normalizeServiceName(keyword) !== normalizeServiceName(brand))
}));

function hasKeyword(normalizedName, keyword) {
  const normalizedKeyword = normalizeServiceName(keyword);
  if (!normalizedKeyword) return false;
  if (normalizedKeyword.length <= 3) {
    return new RegExp(`(^|\\s)${normalizedKeyword.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}(\\s|$)`).test(normalizedName);
  }
  return normalizedName.includes(normalizedKeyword);
}

function getBrandMatch(name) {
  const normalized = normalizeServiceName(name);
  if (!normalized) return null;
  return serviceLogoMappings.find((brand) => brand.keywords.some((keyword) => hasKeyword(normalized, keyword))) ?? null;
}

function getPaymentMatch(name) {
  const normalized = normalizeServiceName(name);
  if (!normalized) return null;
  return paymentMethodMappings.find((method) => method.keywords.some((keyword) => hasKeyword(normalized, keyword))) ?? null;
}

function scoreSuggestions(items, normalized, key) {
  return items
    .map((item) => {
      const searchable = [item.name, ...item.aliases].map(normalizeServiceName);
      const startsWith = searchable.some((value) => value.startsWith(normalized));
      const includes = searchable.some((value) => value.includes(normalized));
      return { [key]: item, score: startsWith ? 2 : includes ? 1 : 0 };
    })
    .filter((item) => item.score > 0)
    .sort((a, b) => b.score - a.score || a[key].name.localeCompare(b[key].name));
}

export function getServiceSuggestions(query = "", limit = 6) {
  const normalized = normalizeServiceName(query);

  if (!normalized) {
    return suggestedServices.slice(0, limit);
  }

  return scoreSuggestions(suggestedServices, normalized, "service")
    .slice(0, limit)
    .map((item) => item.service);
}

export function getPaymentMethodSuggestions(query = "", limit = 8) {
  const normalized = normalizeServiceName(query);

  if (!normalized) {
    return suggestedPaymentMethods.slice(0, limit);
  }

  return scoreSuggestions(suggestedPaymentMethods, normalized, "method")
    .slice(0, limit)
    .map((item) => item.method);
}

export function getServiceDomain(serviceName) {
  const match = getBrandMatch(serviceName);
  return match?.domain ?? null;
}

export function getServicePlanSuggestion(serviceName) {
  const match = getBrandMatch(serviceName);
  if (!match) return null;
  return {
    name: match.brand,
    category: match.category ?? null,
    priceHints: match.priceHints ?? [],
    defaultPrice: match.priceHints?.[0] ?? null
  };
}

function hashName(name = "") {
  return Array.from(name.toString()).reduce((hash, char) => {
    return (hash * 31 + char.charCodeAt(0)) >>> 0;
  }, 7);
}

export function getServiceFallback(name = "") {
  const hash = hashName(normalizeServiceName(name));
  const hue = hash % 360;
  return {
    initials: getSubscriptionInitial(name),
    style: {
      backgroundColor: `hsl(${hue} 88% 96%)`,
      color: `hsl(${hue} 72% 42%)`
    }
  };
}

function buildLogoPayload(match, name, fallbackLabel) {
  const fallback = getServiceFallback(name);

  if (!match?.domain) {
    return {
      brand: match?.brand ?? name?.toString().trim() ?? fallbackLabel,
      domain: null,
      hasLogo: false,
      url: null,
      fallbackUrl: null,
      ...fallback
    };
  }

  return {
    ...match,
    hasLogo: true,
    url: `https://img.logo.dev/${match.domain}?size=128&format=png`,
    fallbackUrl: `https://www.google.com/s2/favicons?sz=128&domain=${match.domain}`,
    ...fallback
  };
}

export function getServiceLogo(name) {
  return buildLogoPayload(getBrandMatch(name), name, "Service");
}

export function getPaymentMethodLogo(name) {
  return buildLogoPayload(getPaymentMatch(name), name, "Payment");
}

export function getSubscriptionInitial(name = "") {
  const trimmed = name.toString().trim();
  if (!trimmed) return "?";
  const words = normalizeServiceName(trimmed).split(" ").filter(Boolean);
  if (words.length > 1) return words.slice(0, 2).map((word) => word[0]).join("").toUpperCase();
  return words[0]?.[0]?.toUpperCase() ?? trimmed[0].toUpperCase();
}

